#!/usr/bin/env node
/**
 * generate-health-report.mjs
 *
 * Reads pre-collected CI check outputs, sends them to the Claude API,
 * and writes a formatted health report to health-report.md.
 *
 * Called by .github/workflows/ai-health-report.yml.
 * Requires ANTHROPIC_API_KEY in the environment.
 * Uses Node 22 native fetch — no extra dependencies needed.
 */

import { readFileSync, writeFileSync, existsSync } from 'fs';

const ANTHROPIC_API_KEY = process.env.ANTHROPIC_API_KEY;

if (!ANTHROPIC_API_KEY) {
  console.error('ERROR: ANTHROPIC_API_KEY environment variable is not set.');
  process.exit(1);
}

/**
 * Safely read a file, returning a fallback string if it doesn't exist.
 * Truncates to maxChars to avoid sending huge payloads to the API.
 */
function readOutput(path, maxChars = 3000) {
  if (!existsSync(path)) return '*(file not found — step may have been skipped)*';
  const content = readFileSync(path, 'utf8').trim();
  if (content.length === 0) return '*(no output)*';
  if (content.length <= maxChars) return content;
  return content.slice(0, maxChars) + `\n\n... (truncated — ${content.length - maxChars} chars omitted)`;
}

const date = new Date().toISOString().slice(0, 10);

const typeCheck = readOutput('type-check-output.txt');
const lint      = readOutput('lint-output.txt');
const tests     = readOutput('test-output.txt', 4000);
const audit     = readOutput('audit-output.txt', 2000);
const outdated  = readOutput('outdated-output.txt', 1500);
const gitLog    = readOutput('git-log.txt', 2000);

const prompt = `You are a senior engineer performing a weekly health review of **starbunk-js** — a TypeScript Discord bot monorepo with four containers: BunkBot (reply bots), DJCova (music), CovaBot (AI personality), and BlueBot (pattern matching). Stack: Node 22, Discord.js 14, TypeScript, PostgreSQL, Redis, Qdrant.

Below are automated check outputs from **${date}**. Write a concise GitHub Issue body in Markdown as the weekly health report. Use these exact sections:

1. **Executive Summary** — 2–3 sentences: overall health, most urgent item if any
2. **Type Safety** — findings from type-check output; call out error counts or confirm clean
3. **Code Quality** — findings from lint output; note error/warning counts or confirm clean
4. **Test Suite** — pass/fail summary; flag any skipped or flaky signals
5. **Security** — npm audit findings; severity breakdown if any vulnerabilities exist
6. **Dependencies** — npm outdated; call out anything significantly stale (major version behind)
7. **Recent Activity** — 2–3 sentences on commit velocity and patterns from the git log
8. **Recommended Actions** — prioritized list of 3–5 concrete, actionable items

Rules:
- Be direct. If a section is clean, say so in one line and move on.
- Don't pad with generic advice. Only flag real problems.
- Use checkboxes (- [ ]) for Recommended Actions so they can be tracked in the issue.

---

## type-check output
\`\`\`
${typeCheck}
\`\`\`

## lint output
\`\`\`
${lint}
\`\`\`

## test suite output
\`\`\`
${tests}
\`\`\`

## npm audit
\`\`\`
${audit}
\`\`\`

## npm outdated
\`\`\`
${outdated}
\`\`\`

## git log (last 2 weeks)
\`\`\`
${gitLog}
\`\`\`
`;

console.log('Sending data to Claude API...');

let response;
try {
  response = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'x-api-key': ANTHROPIC_API_KEY,
      'anthropic-version': '2023-06-01',
      'content-type': 'application/json',
    },
    body: JSON.stringify({
      model: 'claude-haiku-4-5-20251001',
      max_tokens: 2048,
      messages: [{ role: 'user', content: prompt }],
    }),
  });
} catch (err) {
  console.error('Network error calling Anthropic API:', err.message);
  process.exit(1);
}

if (!response.ok) {
  const body = await response.text();
  console.error(`Anthropic API error ${response.status}:`, body);
  process.exit(1);
}

const data = await response.json();
const report = data.content?.[0]?.text;

if (!report) {
  console.error('Unexpected API response shape:', JSON.stringify(data));
  process.exit(1);
}

const fullReport = [
  `> 🤖 *Automated weekly health report — generated by AI on ${date}*`,
  `> Run: [\`${process.env.GITHUB_RUN_ID ?? 'local'}\`](../../actions/runs/${process.env.GITHUB_RUN_ID ?? ''})`,
  '',
  report,
  '',
  '---',
  `*Input tokens: ${data.usage?.input_tokens ?? '?'} / Output tokens: ${data.usage?.output_tokens ?? '?'}*`,
].join('\n');

writeFileSync('health-report.md', fullReport, 'utf8');
console.log('health-report.md written successfully.');
console.log(`Token usage: ${data.usage?.input_tokens} in / ${data.usage?.output_tokens} out`);
